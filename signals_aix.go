import (
	"log"
	"os"
	"os/signal"
	"runtime/debug"
	"syscall"
)

///////////////////////////////////////////////////////////////////////////////////////////////////

func lowerGOGC() {
	currentGOGC := debug.SetGCPercent(-1)
	newGOGC := currentGOGC / 2
	if newGOGC <= 1 {
		newGOGC = 1
	}
	debug.SetGCPercent(newGOGC)
}

///////////////////////////////////////////////////////////////////////////////////////////////////

func runSignalHandlers() {
	sigChan := make(chan os.Signal, 1)
	signal.Notify(sigChan,
		syscall.SIGINT, syscall.SIGTERM, syscall.SIGQUIT,
		syscall.SIGHUP, syscall.SIGUSR1, syscall.SIGUSR2, syscall.SIGDANGER)

	go func() {
		for s := range sigChan {
			if s == syscall.SIGDANGER {
				log.Printf(
					"%sSIGDANGER received: Requesting garbage collection and freeing memory.\r\n",
					boomPrefix())
				debug.FreeOSMemory()
				lowerGOGC()
			} else {
				handleSignal(s)
			}
		}
	}()
}

