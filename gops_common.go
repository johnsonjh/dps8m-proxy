package main

///////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"log"

	"github.com/google/gops/agent"
)

///////////////////////////////////////////////////////////////////////////////////////////////////

const gopsEnabled = true

///////////////////////////////////////////////////////////////////////////////////////////////////

func gopsInit() {
	go func() {
		defer recoverGoroutine("gopsInit")

		err := agent.Listen(
			agent.Options{
				ReuseSocketAddrAndPort: true,
				ShutdownCleanup:        false,
			},
		)
		if err != nil {
			log.Printf("%sFailed to start gops agent: %v",
				warnPrefix(), err)
		}
	}()
}

///////////////////////////////////////////////////////////////////////////////////////////////////

func gopsClose() {
	agent.Close()
}

